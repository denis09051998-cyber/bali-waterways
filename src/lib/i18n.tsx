const DICTS: Record<Lang, Dict> = { en, ru, id };

type Ctx = { lang: Lang; setLang: (l: Lang) => void; t: (k: string) => string };
const I18nCtx = createContext<Ctx>({ lang: "en", setLang: () => {}, t: (k) => k });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");
  useEffect(() => {
    try {
      const saved = localStorage.getItem("unity.lang") as Lang | null;
      if (saved && DICTS[saved]) setLangState(saved);
      else {
        const nav = navigator.language?.slice(0, 2).toLowerCase();
        if (nav === "ru" || nav === "id") setLangState(nav as Lang);
      }
    } catch {}
  }, []);
  useEffect(() => {
    try { document.documentElement.lang = lang; } catch {}
  }, [lang]);
  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem("unity.lang", l); } catch {}
  };
  const t = (k: string) => DICTS[lang][k] ?? DICTS.en[k] ?? k;
  return <I18nCtx.Provider value={{ lang, setLang, t }}>{children}</I18nCtx.Provider>;
}

export function useI18n() {
  return useContext(I18nCtx);
}
